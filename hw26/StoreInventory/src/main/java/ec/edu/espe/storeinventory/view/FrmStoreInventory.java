    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String name = txtProduct.getText().trim();
            double price = Double.parseDouble(txtPrice.getText());
            int stock = Integer.parseInt(txtStock.getText());

            if (name.isEmpty() || price <= 0 || stock < 0) {
                JOptionPane.showMessageDialog(this, "Error: Name cannot be empty, price must be > 0, and stock cannot be negative.");
                return;
            }

            Product product = new Product(name, price, stock);
            InventoryController controller = new InventoryController();
            controller.addProduct(product);

            JOptionPane.showMessageDialog(this, "Product saved successfully!");
            refreshTable();
            clearFields();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Please enter valid numbers.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String name = txtProduct.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select a product from the table or type its name.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete '" + name + "'?");
        if (confirm == JOptionPane.YES_OPTION) {
            InventoryController controller = new InventoryController();
            controller.deleteProduct(name);
            refreshTable();
            clearFields();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String name = txtProduct.getText().trim();
            int newStock = Integer.parseInt(txtStock.getText());

            if (newStock < 0) {
                JOptionPane.showMessageDialog(this, "Error: Stock cannot be negative.");
                return;
            }

            InventoryController controller = new InventoryController();
            controller.updateStock(name, newStock);

            JOptionPane.showMessageDialog(this, "Stock updated successfully.");
            refreshTable();
            clearFields();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Enter a valid integer for units.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            txtProduct.setText(jTable1.getValueAt(selectedRow, 0).toString());
            txtStock.setText(jTable1.getValueAt(selectedRow, 1).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

