    private void refreshTable() {
        try {
            InventoryController controller = new InventoryController();
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Product Name");
            model.addColumn("Stock Units");
            model.addColumn("Sales Price (Tax)");

            for (String item : controller.getInventoryList()) {
                String[] data = item.split(" \\| ");
                String name = data[0];
                String stock = data[1].replace("Stock: ", "");
                String price = data[2].replace("PVP: ", "").replace("Price: ", "");
                model.addRow(new Object[]{name, stock, price});
            }

            jTable1.setModel(model);

            double total = controller.calculateTotalInventoryValue();
            lblTotalValue.setText("Total Warehouse Value: $" + String.format("%.2f", total));
        } catch (Exception e) {
            System.err.println("Database connection error: " + e.getMessage());
        }
    }

    private void clearFields() {
        txtProduct.setText("");
        txtPrice.setText("");
        txtStock.setText("");
        txtProduct.requestFocus();
    }

